package solidOCP;

public class Rectangle implements Shape {

    private int x;
    private int y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    @Override
    public void getArea() {
        System.out.println(x*y);
    }
}
